private boolean validateInput(Customer u)
{
    if(u.getAccHolderName().isEmpty()||u.getAge().isEmpty()||u.getAccType().isEmpty()||u.getContactno().isEmpty()||u.getDob().isEmpty()||u.getNomineeName().isEmpty()||u.getPanNo().isEmpty()||u.getAdharNo().isEmpty()||u.getPerAddress().isEmpty()||u.getTempAddress().isEmpty())
  {
    return false;
  }
  else
  {
    return true;
  }
}
