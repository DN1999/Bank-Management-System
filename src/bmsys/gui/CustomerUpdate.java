    private void jTextField4KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyPressed
        // TODO add your handling code here:
        char c=evt.getKeyChar();
        String no=jTextField4.getText();
        int len=no.length();
        if(Character.isDigit(c))
        {
            if(len<10)
            {
                jTextField4.setEditable(true);
            }
            else
            {
                jTextField4.setEditable(false);
            }
        }else
        {
            if(Character.isWhitespace(c)||Character.isISOControl(c))
            jTextField4.setEditable(true);
            else
            jTextField4.setEditable(false);
        }

        ClearAllField();        
    }//GEN-LAST:event_jTextField4KeyPressed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Customer c=new Customer();
        String accno=jTextField4.getText();
        try
        {
            if(jTextField4.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null,"Please Provide Account Number..","Error",JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                c=Customerdao.SearchCustomer(accno);
                jTextField1.setText(c.getAccHolderName());
                jTextField2.setText(c.getNomineeName());
                //jDateChooser1.setDate("");
                //jDateChooser1.setDate(date);
                ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).setText(c.getDob());
                jComboBox1.setSelectedItem(c.getAge());
                jComboBox2.setSelectedItem(c.getAccType());
                 gender = c.getGender();
                        if(gender.equals("Male"))
                        {
                         MaleRB.setSelected(true);
                         FemaleRB.setSelected(false);
                         OtherRB.setSelected(false); 
                        }else if(gender.equals("Female"))
                        {
                            MaleRB.setSelected(false);
                            FemaleRB.setSelected(true);
                            OtherRB.setSelected(false);
                        }else if(gender.equals("Other"))
                        {
                            MaleRB.setSelected(false);
                            FemaleRB.setSelected(false);
                            OtherRB.setSelected(true);
                        } 
                jTextField7.setText(c.getEmail());
                jTextField5.setText(c.getContactno());
                jTextField3.setText(c.getPanNo());
                jTextField6.setText(c.getAdharNo());
                jTextField11.setText(c.getState());
                jTextField10.setText(c.getCountry());
                jTextArea1.setText(c.getTempAddress());
                jTextArea2.setText(c.getPerAddress());
                }
        }catch(Exception e)
        {

        }
    }//GEN-LAST:event_jButton3ActionPerformed

